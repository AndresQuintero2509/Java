
public class CaracteresEspeciales {

    public static void main(String args[]) {
        var nombre = "Ana Maria";

        System.out.println("Nueva linea: \n" + nombre); // Imprime en una nueva linea
        System.out.println("Tabulador: \t" + nombre); // Imprime un tabulador
        System.out.println("Retroceso: \b" + nombre); // Borra un espacio
        System.out.println("Comilla Simple: '" + nombre + "'"); // Imprime comillas simples
        System.out.println("Comilla doble: \"" + nombre + "\""); // Imprime comillas dobles
    }
}
